    private void btnfiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnfiltrarActionPerformed
        // TODO add your handling code here:
        int cod=Integer.parseInt(idcliente.getText());
        Cliente b=obj.busca(cod);
        if(b==null){
            JOptionPane.showMessageDialog(null,"no existe codigo");
            return;
        }
        idcliente.setText(b.getIdCliente()+"");
        txtnombre.setText(b.getNombre());
        cbtipodoc.setSelectedItem(b.getTipoDocumento().toString());
        txtNdoc.setText(b.getNroDocumento());
        txttelefono.setText(b.getTelefono());
    }//GEN-LAST:event_btnfiltrarActionPerformed

