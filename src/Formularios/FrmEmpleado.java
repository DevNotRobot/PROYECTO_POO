    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String nombre;
        String tipoDocumento;
        String nroDocumento;
        String telefono;
        String cargo;
        String estado;
        int IdUsuario;
        nombre=(txtnombre.getText());
        tipoDocumento=(cbtipodoc.getSelectedItem().toString());
        nroDocumento=(txtNdoc.getText());
        telefono=(txttelefono.getText());
        cargo=(txtCargo.getText());
        estado=(cbEstado.getSelectedItem().toString());
        IdUsuario=Integer.parseInt(txtIdusuario.getText());
        Empleado p= new Empleado(cargo, estado, IdUsuario, nombre, tipoDocumento, nroDocumento, telefono);

        obj.adicion(p);
        btnAgregar.setEnabled(false);
        muestra();
    }//GEN-LAST:event_btnAgregarActionPerformed

